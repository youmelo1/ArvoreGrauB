package arquivo;
public class Gravador {
    String nome;
    String cpf;
    String rg;
    String data;
    String cidade;

    public Gravador (String nome, String cpf, String rg, String data, String cidade){
        this.nome = nome;
        this.cpf = cpf;
        this.rg = rg;
        this.data = data;
        this.cidade = cidade;
    }
}
